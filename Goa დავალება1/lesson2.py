from turtle import *

# we want to paint a house
speed(10)
width(5)

#  draw a grass
penup()
goto(-400, -100)
pendown()
color("green")
begin_fill()
forward(800)
right(90)
forward(200)
right(90)
forward(800)
right(90)
forward(200)
end_fill()

#  draw a square
penup()
goto(-100, -100)
pendown()
color("red")
begin_fill()
forward(200) 
right(90)
forward(200) 
right(90)
forward(200) 
right(90)
forward(200) 
end_fill()
# draw a door
penup()
goto(-30, -100)
pendown()
color("brown")
begin_fill()
right(90)
forward(120)
right(90)
forward(60)
right(90)
forward(120)
right(90)
forward(60)
end_fill()

penup()
goto(-120, 100)
pendown()
color("blue")
begin_fill()
goto(120, 100)
goto(0, 190)
goto(-120, 100)
end_fill()

# window left
penup()
goto(-80, 50)
color("yellow")
begin_fill()
pendown()
right(90)
forward(40)
right(90)
forward(40)
right(90)
forward(40)
right(90)
forward(40)
# window right
penup()
goto(40, 50)
pendown()
right(90)
forward(40)
right(90)
forward(40)
right(90)
forward(40)
right(90)
forward(40)
end_fill()


exitonclick()